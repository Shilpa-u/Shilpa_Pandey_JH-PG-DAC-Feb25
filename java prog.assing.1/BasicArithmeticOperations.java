public class BasicArithmeticOperations{
          public static void main(String[] args){

          int a,b,sum,multiplication,substraction,divison,remainder;

          a = 125;
          b = 24;
         
         sum = a+b;
         System.out.println(sum);
         
         substraction = a-b;
         System.out.println(a-b);

         multiplication = a*b;
         System.out.println(a*b);

         divison = a/b;
         System.out.println(a/b);

         remainder = a%b;
         System.out.println(a%b);

}
}



