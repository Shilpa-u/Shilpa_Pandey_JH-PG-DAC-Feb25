public class BooleanControlFlow{
public static void main (String[] args){
boolean isRainy = true;
boolean isSunny = false;
if (isRainy){
   System.out.println("It's rainy,take an umbrella!");
   }
   else {
   System.out.println("It's not rainy,enjoy!");
   }
   if (isSunny) { 
   System.out.println("It's sunny ,go outside!");
   }else{
   System.out.println("It's not sunny ,stay inside!");
   }
   }
   }